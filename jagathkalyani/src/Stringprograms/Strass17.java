package Stringprograms;

public class Strass17 {
    public static void main(String[] args) {
        String name="warriors";
        System.out.println("characters in " + name + "are:");
        for(int i=0;i<name.length();i++){
            char a=name.charAt(i);
            System.out.print(a + " , ");
        }
    }
    
}
