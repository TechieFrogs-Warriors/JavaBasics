public class Pyramids1 {
    public static void main(String[] args) {
        int i,j;
        for(i=1;i<=5;i++)
        {
            for(j=1;j<=i;j++)
            {
                System.out.print("*");

            }
            System.out.println("");
            
        }
        
        int m,n;
        for(m=1;m<=5;m++)
        {
            for(n=1;n<=m;n++)
            {
                System.out.print(n);

            }
            System.out.println("");
        }
        
        
    }
    
}
