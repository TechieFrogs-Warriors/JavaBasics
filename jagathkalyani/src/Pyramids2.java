import jdk.incubator.jpackage.main.Main;

public class Pyramids2 {
    public static void main(String[] args) {
        int i,j;
        for(i=1;i<=5;i++)
        {
            for(j=1;j<=5;j++)
            {
                System.out.print("*");

            }
            System.out.println("");
            
        }
        int k,l;
        for(k=1;k<=5;k++)
        {
            for(l=1;l<=5;l++)
            {
                System.out.print(l);

            }
            System.out.println("");
            
        }
    }
    
}
