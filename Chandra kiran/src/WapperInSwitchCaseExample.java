public class WapperInSwitchCaseExample {
    public static void main(String[] args) {
        Integer Standard=8;
        switch(Standard){
            case 1: System.out.println("1st standard");
            break;
            case 2: System.out.println("2nd standard");
            break;
            case 3: System.out.println("3rd standard");
            break;
            case 4:System.out.println("4th standard");
            break;
            default:System.out.println("6th standard");

        }

    }
    
}
