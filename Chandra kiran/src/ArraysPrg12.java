public class ArraysPrg12 
{
    public static void main(String[] args) 
    {
        int a[]={10,30,50,70,90};
        int sum=0;
        for(int i=0;i<a.length;i++)
        {
            sum=sum+a[i];//add each element to sum

        }
        System.out.println("Sum of elements "+sum);

        
    }
    
}
