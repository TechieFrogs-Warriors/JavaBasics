public class NestedForExample {
    public static void main(String[] args) {
        int i,j,term=5;
        for( i=1;i<=term;i++)
        {
            for(j=term;j>=i;j--)
            {
            
                
                System.out.print(j);

            }
        
        System.out.println();
        }

    }
    
}
