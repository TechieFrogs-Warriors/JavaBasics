package ArrayAssignmentPrograms;

public class SumOfTheElements12 {
    public static void main(String[] args) {
        int a[] = {12,8,19,17,5}; 
        int sum=0;   
        for (int i=0;i<a.length;i++) {  
           sum=sum+a[i];  
        }  
        System.out.println("Sum of the Elements : " +sum);  
     
    }
    
}
