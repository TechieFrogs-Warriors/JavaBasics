public class Comparison {
    public static void main(String[] args) {
        int x = 20;
        int y = 50;
        System.out.println("---------comparison operators---------");
        System.out.println("x > y :" + (x > y));
        System.out.println("x < y :" + (x < y));
        System.out.println("x == y :" + (x == y));
        System.out.println("x != y :" + (x != y));
        System.out.println("x >= y :" + (x >= y));
        System.out.println("x <= y :" + (x <= y));
        int i = 20;
        int j = 20;
        System.out.println("i == j :" + (i == j));
        System.out.println("i != j :" + (i != j));
        System.out.println("i >= j :" + (i >= j));
        System.out.println("i <= j :" + (i <= j));
        System.out.println("i > j :" + (i > j));
        System.out.println("i < j :" + (i < j));

    }

}
