package Arraysprograms;

public class Program2 {
    public static void main(String[] args) {
        int a[] = { 1, 2, 3, 4, 5, 6, };

        Double b[] = { 22.3, 25.336, 556.35 };

        String c[] = { "sai", "venkata", "Krishna", "Sandeep" };

        for (int i : a) {
            System.out.print(i + " ");

        }
        System.out.println("\n\n");
        for (double j : b) {
            System.out.print(j + " ");

        }
        System.out.println("\n\n");
        for (String i : c) {
            System.out.print(i + " ");

        }
    }

}
