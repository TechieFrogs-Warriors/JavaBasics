package TypeCasting;

public class Program11 {
    public static void main(String[] args) {
        // type casting int to byte
        byte b = 70;

        b = (byte) (b * 2);
        System.out.println("b value : " + b);

        byte b1 = 10;
        b1 = (byte) (b1 * 2);
        System.out.println("b1 value : " + b1);
        // type casting using int
        int i = 325252314;
        i = (int) (i * 20);
        System.out.println("i value : " + i);
    }

}
