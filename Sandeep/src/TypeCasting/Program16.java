package TypeCasting;

public class Program16 {
    public static void main(String[] args) {
        System.out.println("String to object");
        String s = "sai sandeep";
        Object obj = s;
        System.out.println(obj);
        System.out.println("Integer to object");
        int i = 253;
        Object obj1 = i;
        System.out.println(obj1);
        System.out.println("float to object");
        float f = 225.32f;
        Object obj2 = f;
        System.out.println(obj2);

    }
}
