package StringsPrograms;

public class Program9 {
    public static void main(String[] args) {
        String s1 = "Sandeep ";
        String s2 = "Sai";
        System.out.println(s1.charAt(0));// S
        System.out.println(s1.charAt(6));// p
        System.out.println(s1.charAt(7));// prints space
        System.out.println((s1 + s2).charAt(9));// a
        System.out.println((s1.substring(2, 7).charAt(2)));// e

    }

}
