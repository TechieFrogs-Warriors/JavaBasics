package StringsPrograms;

public class Program10 {
    public static void main(String[] args) {
        String s1 = "Sandeep";
        String s2 = "Sai";
        System.out.println(s1.length());// 7
        System.out.println((s1 + s2).length());// 10
        System.out.println((s1.substring(1, 5).length()));// 4
    }

}
