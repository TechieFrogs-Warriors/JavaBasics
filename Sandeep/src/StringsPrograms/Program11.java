package StringsPrograms;

public class Program11 {
    public static void main(String[] args) {
        int a = 500;
        float b = (float) 2.5;
        double c = 225.35;
        char ch = 'a';
        System.out.println((String.valueOf(a)) + 10);// 50010
        System.out.println((String.valueOf(b)) + 10);// 2.510
        System.out.println((String.valueOf(c)) + 10);// 225.3510
        System.out.println((String.valueOf(ch)) + 10);// a10

    }

}
