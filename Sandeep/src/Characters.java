public class Characters {
    public static void main(String[] args) {
        System.out.println("--- Dispalying Upper Cased A to Z ---");

        for (char i = 'A'; i <= 'Z'; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        System.out.println("--- Dispalying Lower Cased a to z ---");

        for (char j = 'a'; j <= 'z'; j++) {
            System.out.print(j + " ");
        }
    }
}
