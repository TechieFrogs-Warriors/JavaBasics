public class DefaultConstructorSample 
{
    int num; 
    String name;
    boolean value;
    
    public static void main(String[] args) 
    {
       //default constructor is invoked when creating object to class
        DefaultConstructorSample obj=new DefaultConstructorSample();
       System.out.println("INTEGER VALUE IS: "+obj.num);
       System.out.println("STRING VALUE IS: "+obj.name);
       System.out.println("BOOLEAN VALUE IS: "+obj.value);
        
    }     
     
} 
  
    

