
public class AlphabetPrinting 
{
    public static void main(String[] args)
     {
        System.out.println("PRINTING ALPHABETS FROM a-z");
        for(char alphabet='a';alphabet<='z';alphabet++)
        {
            System.out.print(alphabet+" ");
        }
        System.out.println();
        System.out.println("PRINTING ALPHABETS FROM A-Z");
        for(char alphabet='A';alphabet<='Z';alphabet++)
        {
            System.out.print(alphabet+" ");
        }

    }
     
}
